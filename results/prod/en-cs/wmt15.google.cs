Opětovné propojení s velmi americkým ideálem, že pracovní práva jsou lidská práva
Kongresmani Keith Ellison a John Lewis navrhli právní předpisy na ochranu organizování odborů jako občanské právo.
„Jak fungují odbory, tak fungují práce ze střední třídy,“ říká Ellison, demokrat z Minnesoty, který je spolupředsedou Kongresového progresivního sněmu.
Proto jsem hrdý na to, že mohu představit zákon o zmocnění zaměstnanců s ikonou občanských práv Johnem Lewisem.
Tato průkopnická legislativa poskytne pracovníkům stejné právní možnosti pro organizaci diskriminace v odborech jako pro jiné formy diskriminace - zastavení sil odborů v jejich stopách
Novela národního zákona o pracovněprávních vztazích umožňující pracovníkům, kteří čelí diskriminaci za účast v organizování odborů, žalovat před soudem u civilních soudů - a vymáhat náhradu škody a náhradu škody - je rozumnou a nezbytnou iniciativou.
Rozhodně to však není radikální iniciativa - přinejmenším podle amerických standardů.
Nejlepší způsob, jak pochopit, co Ellison, Lewis a spoluzakladatelé jejich právních předpisů navrhují, je opětovné spojení s velmi americkou myšlenkou.
Navzdory týrání, které odbory v posledních letech podnikly - ve Wisconsinu, Michiganu a státech po celé zemi - Američané kdysi vyzývali země po celém světě, aby přijaly, rozšířily a respektovaly pracovní práva.
V živé paměti milionů Američanů byla doba, kdy tato země prosazovala demokracii, svobodu projevu, svobodu tisku a právo organizovat se stejným dechem.
Když USA po druhé světové válce okupovaly Japonsko, generál Douglas MacArthur a jeho pomocníci vyzvali zemi, aby přijala ústavu, která zajistí, že militarizovaná autokracie Hidekiho Toja bude nahrazena demokracií.
Plně vědomi toho, že při formování nového Japonska mají hrát roli pracovníci a jejich odbory, zahrnuli jazyk, který výslovně uznává, že „je zaručeno právo pracovníků organizovat se a vyjednávat a jednat společně“.
Když Spojené státy po druhé světové válce okupovaly Německo, generál Dwight David Eisenhower a jeho pomocníci vyzvali Němce, aby vytvořili ústavu, která zajistí, že fašismus Adolfa Hitlera bude nahrazen svalovou demokracií.
Němci si uvědomili, že pracovníci budou muset v novém národě zorganizovat a nechat slyšet svůj hlas, a zahrnovali ustanovení, které výslovně prohlásilo: „Právo na zakládání a zlepšování pracovních a hospodářských podmínek bude zaručeno každému jednotlivci a každé profesi nebo povolání.
Dohody, které omezují nebo usilují o narušení tohoto práva, jsou neplatné; opatření směřující k tomuto účelu jsou protiprávní.
Když bývalá první dáma Eleanor Rooseveltová předsedala Mezinárodní komisi pro lidská práva, která vypracovala Všeobecnou deklaraci lidských práv, která bude v roce 1948 přijata Organizací spojených národů jako globální smlouva, Roosevelt a autoři zahrnovali záruku, že „každý má právo zakládat a vstupovat do odborů na ochranu svých zájmů. “
